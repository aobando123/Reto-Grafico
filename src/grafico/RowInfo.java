package grafico;

/**
 *
 * @author Adri
 */
public class RowInfo {
    private String name;
    private int value;

    public RowInfo(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

   
    
}
